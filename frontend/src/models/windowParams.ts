export default class WindowParams {
    width: number = 390;
    height: number = 500;
    x: number = 100;
    y: number = 100;

    constructor(width: number, height: number, x: number, y: number) {
        this.width = width;
        this.height = height;
        this.x = x;
        this.y = y;
    }
}
